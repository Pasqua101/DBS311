  CREATE TABLE players (
    playerid INT PRIMARY KEY, 
	regnumber VARCHAR2(15), 
	lastname VARCHAR2(25), 
	firstname VARCHAR2(25),
	isactive INT DEFAULT 1
   );
   
CREATE TABLE teams (	
    teamid INT PRIMARY KEY, 
	teamname VARCHAR(10),
	isactive INT, 
	jerseycolour VARCHAR(10)
   );
   
CREATE TABLE divs (
	divid INT PRIMARY KEY, 
	divname VARCHAR2(3), 
	isActive INT, 
	isDefault INT, 
	displayOrder INT DEFAULT 10
   );
   
CREATE TABLE rosters (	
	rosterid INT GENERATED by default on null as IDENTITY(START with 1 INCREMENT by 1) PRIMARY KEY, 
	playerid INT, 
	teamid INT, 
	Isactive INT, 
	jerseynumber INT DEFAULT 0
   );
   
-- CREATE teamsInDivs table
CREATE TABLE teamsInDivs
(
    linkid INT GENERATED BY DEFAULT ON NULL AS IDENTITY(START with 1 INCREMENT by 1) PRIMARY KEY,
    teamid INT NOT NULL,
    divid INT NOT NULL
);

CREATE TABLE games (
    gameid INT GENERATED by default on null as IDENTITY(START with 1 INCREMENT by 1) PRIMARY KEY, 
	divid INT, 
	Gamenum INT, 
	gamedatetime DATE, 
	hometeam INT, 
	homescore INT, 
	visitteam INT, 
	visitscore INT, 
	locationid INT, 
	Isplayed INT, 
	notes VARCHAR2(50)
   ) ;
   
